    private void TableFilling() {
        
    DefaultTableModel model = (DefaultTableModel) bill.getModel();
        model.setRowCount(0);
        for(Bills en : ecoSystem.getBillsList().getBillsList()){
                
            Object[] row = new Object[5];
            row[0] = en.getBillid();
            row[1] = en.getBillname();
            row[2] = en.getPerson().getFirstName()+" "+en.getPerson().getLastName();
            row[3] = en.getAmount();
            row[4] = en.getStatus();
           
           
            
            model.addRow(row);
            
             }
    
    }
