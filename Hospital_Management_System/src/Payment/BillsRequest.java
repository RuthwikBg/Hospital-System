    private void rolesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rolesActionPerformed
        // TODO add your handling code here:
        
        String cityName = roles.getSelectedItem()==null?"":roles.getSelectedItem().toString();
        ArrayList<String> communityBoxList = new ArrayList<String>();
        if(cityName=="Operation Manager")
        {
           for(Operation_Manager oper:ecoSystem.getOperationmanagerdir().getOpManagerList()){
           names.addItem(oper.getFirstName()+" "+oper.getLastName());
           }
        }
        else if(cityName=="Bed Manager")
        {
        for(Bed_Manager oper:ecoSystem.getBedmanagerdir().getBedManagerList()){
           names.addItem(oper.getFirstName()+" "+oper.getLastName());
           }
        }
        else if (cityName=="Pharmacist")
        {
        for(Pharmacist oper:ecoSystem.getPharmacist().getPharmacistList()){
           names.addItem(oper.getFirstName()+" "+oper.getLastName());
           }
        }
        
    }//GEN-LAST:event_rolesActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Bills b = new Bills();
        b.setAmount(Integer.valueOf(amount.getText()));
        b.setBillid(billid.getText());
        b.setBillname(billname.getText());
        for(Person person : ecoSystem.getPersondir().getPersonList()){
         if(names.getSelectedItem().toString().equals(person.getFirstName()+" "+person.getLastName()))
         {  b.setPerson(person);}}
        b.setStatus(status.getSelectedItem().toString());
    }//GEN-LAST:event_jButton1ActionPerformed

